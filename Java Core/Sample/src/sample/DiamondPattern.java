package sample;

public class DiamondPattern {
	public static void main(String[] args) {
		for(int i=0;i<=10;i++) {
			for(int j=10;j>i;j--) {
				
				System.out.print(" ");
			}
				for(int k=i;k<=4;k++) {
					i=i+2;
					System.out.print("*");
//					
					
					
				}
				System.out.println();
				
			}
		}
	}

			

	
	
	

